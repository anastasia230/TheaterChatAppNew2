package com.example.theaterchatappnew2;

public class BookingInfo {
    public String title;
    public String datetime;
    public String seat;
    public String code;
    public String name;

    public BookingInfo(String title, String datetime, String seat, String code, String name) {
        this.title = title;
        this.datetime = datetime;
        this.seat = seat;
        this.code = code;
        this.name = name;
    }
}
